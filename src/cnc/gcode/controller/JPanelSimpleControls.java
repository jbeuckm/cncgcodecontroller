    private void appendToConsole(String text)
    {
        String[] lines=ConsoleOutput.getText().split("\n\r");
        String newtext="";
        for(int i=Math.max(0, lines.length-18);i<lines.length;i++)
            if(!lines[i].equals(""))
                newtext+=lines[i]+"\n\r";
        newtext+=text;
        ConsoleOutput.setText(newtext);
    }
